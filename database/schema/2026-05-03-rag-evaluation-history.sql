SET search_path TO ai_registration;

CREATE TABLE IF NOT EXISTS knowledge_evaluation_run (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    trace_id VARCHAR(120),
    base_url VARCHAR(500),
    cases_path VARCHAR(1000),
    total_count INTEGER NOT NULL DEFAULT 0,
    passed_count INTEGER NOT NULL DEFAULT 0,
    failed_count INTEGER NOT NULL DEFAULT 0,
    pass_rate NUMERIC(8, 4) NOT NULL DEFAULT 0,
    by_group JSONB NOT NULL DEFAULT '[]'::jsonb,
    metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
    generated_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS knowledge_evaluation_case_result (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID NOT NULL REFERENCES knowledge_evaluation_run(id) ON DELETE CASCADE,
    case_id VARCHAR(200) NOT NULL,
    group_name VARCHAR(80),
    namespace VARCHAR(120),
    passed BOOLEAN NOT NULL DEFAULT FALSE,
    status VARCHAR(60),
    expected_status VARCHAR(60),
    hit_count INTEGER NOT NULL DEFAULT 0,
    matched_rank INTEGER,
    best_score DOUBLE PRECISION,
    message TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_knowledge_evaluation_run_created
    ON knowledge_evaluation_run (created_at DESC);

CREATE INDEX IF NOT EXISTS idx_knowledge_evaluation_run_trace
    ON knowledge_evaluation_run (trace_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_knowledge_evaluation_case_run
    ON knowledge_evaluation_case_result (run_id, passed, group_name);

COMMENT ON TABLE knowledge_evaluation_run IS 'RAG evaluation run history for regression checks and release gates.';
COMMENT ON COLUMN knowledge_evaluation_run.id IS 'Evaluation run primary key.';
COMMENT ON COLUMN knowledge_evaluation_run.trace_id IS 'Trace ID used when running the evaluation.';
COMMENT ON COLUMN knowledge_evaluation_run.base_url IS 'Knowledge service base URL used by the evaluation script.';
COMMENT ON COLUMN knowledge_evaluation_run.cases_path IS 'Evaluation case definition path or identifier.';
COMMENT ON COLUMN knowledge_evaluation_run.total_count IS 'Total evaluated case count.';
COMMENT ON COLUMN knowledge_evaluation_run.passed_count IS 'Passed evaluated case count.';
COMMENT ON COLUMN knowledge_evaluation_run.failed_count IS 'Failed evaluated case count.';
COMMENT ON COLUMN knowledge_evaluation_run.pass_rate IS 'Passed count divided by total count.';
COMMENT ON COLUMN knowledge_evaluation_run.by_group IS 'Grouped evaluation summary in JSON format.';
COMMENT ON COLUMN knowledge_evaluation_run.metadata IS 'Reserved metadata for model, embedding and release context.';
COMMENT ON COLUMN knowledge_evaluation_run.generated_at IS 'Timestamp generated by the evaluation runner.';
COMMENT ON COLUMN knowledge_evaluation_run.created_at IS 'Database creation timestamp.';

COMMENT ON TABLE knowledge_evaluation_case_result IS 'Per-case RAG evaluation result details.';
COMMENT ON COLUMN knowledge_evaluation_case_result.id IS 'Evaluation case result primary key.';
COMMENT ON COLUMN knowledge_evaluation_case_result.run_id IS 'Parent evaluation run ID.';
COMMENT ON COLUMN knowledge_evaluation_case_result.case_id IS 'Stable evaluation case ID.';
COMMENT ON COLUMN knowledge_evaluation_case_result.group_name IS 'Evaluation group, such as triage, guide or registration-policy.';
COMMENT ON COLUMN knowledge_evaluation_case_result.namespace IS 'Knowledge namespace queried by the case.';
COMMENT ON COLUMN knowledge_evaluation_case_result.passed IS 'Whether this case satisfied all expectations.';
COMMENT ON COLUMN knowledge_evaluation_case_result.status IS 'Actual retrieval status.';
COMMENT ON COLUMN knowledge_evaluation_case_result.expected_status IS 'Expected retrieval status.';
COMMENT ON COLUMN knowledge_evaluation_case_result.hit_count IS 'Returned hit count.';
COMMENT ON COLUMN knowledge_evaluation_case_result.matched_rank IS 'Rank of the first matching hit within expected attributes.';
COMMENT ON COLUMN knowledge_evaluation_case_result.best_score IS 'Best returned hit score.';
COMMENT ON COLUMN knowledge_evaluation_case_result.message IS 'Failure or diagnostic message.';
COMMENT ON COLUMN knowledge_evaluation_case_result.created_at IS 'Database creation timestamp.';
